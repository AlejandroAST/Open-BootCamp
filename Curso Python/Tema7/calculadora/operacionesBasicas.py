def suma(a: int, b: int) -> int:
    """
    Realiza la suma de los parametros recibidos
    :param a: int
    :param b: int
    :return: int
    """
    return a + b


def resta(a: int, b: int) -> int:
    """
    Realiza la resta de los parametros recibidos
    :param a: int
    :param b: int
    :return: int
    """
    return a - b


def multiplicacion(a: int, b: int) -> int:
    """
    Realiza la multiplicacion de los parametros recibidos
    :param a: int
    :param b: int
    :return: int
    """
    return a * b


def division(a: int, b: int) -> int:
    """
    Realiza la division de los parametros recibidos
    :param a: int
    :param b: int
    :return: int
    """
    return a // b
